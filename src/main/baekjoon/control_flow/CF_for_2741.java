package main.baekjoon.control_flow;

import java.util.Scanner;

public class CF_for_2741 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();

        for (int i = 1; i <= a; i++) {
            System.out.println(i);
        }
    }
}
